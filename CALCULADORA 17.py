#CALCULAR LA ALTURA DE UN TRAPECIO
altura=0.0
area=0.0
base_mayor=0.0
base_menor=0.0

#ASIGNACION DE VALORES
area=100.0
base_mayor=25.0
base_menor=15.0

#CALCULO
altura=area*2/(base_mayor+base_menor)

#MOSTRAR CALCULO
print("area=",area)
print("base_mayor=",base_mayor)
print("base_menor=",base_menor)
print("altura=",altura)
